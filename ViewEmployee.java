    private void tbl_ViewEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_ViewEmployeeMouseClicked
        int index=tbl_ViewEmployee.getSelectedRow();
        TableModel model=tbl_ViewEmployee.getModel();
        int empID=(Integer)model.getValueAt(index, 0);
        int a;
        a = JOptionPane.showConfirmDialog(null,"Are you sure to delete this employee","Select",JOptionPane.YES_NO_OPTION);
        if(a==0)
        {
            try{
                Connection con=ConnectionProvider.getcon();
                PreparedStatement ps=con.prepareStatement("delete from EmployeeDetails where employeeID='"+empID+"'");
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Employee deleted successfully");
                setVisible(false);
                new ViewEmployee().setVisible(true);
                con.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_tbl_ViewEmployeeMouseClicked

    private void btn_previewOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_previewOrdersMouseClicked
        DefaultTableModel model=(DefaultTableModel)tbl_ViewEmployee.getModel();
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from EmployeeDetails");
            while(rs.next()){
                    model.addRow(new Object[]{rs.getInt("employeeID"),rs.getString("employeeName"),rs.getString("employeephoneNumber"),rs.getDate("employeeDOB"),rs.getString("employeeGender"),rs.getString("employeeAddress"),rs.getString("employeeCNIC"),rs.getDate("employeeHiredDate")});
            }
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btn_previewOrdersMouseClicked

