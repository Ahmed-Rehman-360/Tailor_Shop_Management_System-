    private void btn_previewOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_previewOrdersMouseClicked
        DefaultTableModel model=(DefaultTableModel)viewOrders.getModel();
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st1=con.createStatement();
            ResultSet rs1=st1.executeQuery("select * from  OrderDetails");
            while(rs1.next()){
                int ID=rs1.getInt("customerID");
                Statement st2=con.createStatement();
                ResultSet rs2=st2.executeQuery("select * from CustomerDetails where customerID='"+ID+"'");
                if(rs2.next()){
                    model.addRow(new Object[]{rs2.getString("customerName"),rs1.getInt("orderID"),rs1.getInt("customerID"),rs1.getString("bookedDate"),rs1.getString("returnDate"),rs1.getInt("no_of_suits"),rs1.getFloat("totalAmount"),rs1.getFloat("paidAmount"),rs1.getFloat("remainingAmount"),rs1.getString("extraNote"),rs1.getString("suitType")});
                }
            }
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btn_previewOrdersMouseClicked

    private void viewOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewOrdersMouseClicked
        int index=viewOrders.getSelectedRow();
        TableModel model=viewOrders.getModel();
        int order_ID=(Integer)model.getValueAt(index, 1);
        int a;
            a = JOptionPane.showConfirmDialog(null,"Are you sure to delete this order","Select",JOptionPane.YES_NO_OPTION);
            if(a==0)
            {
                try{
                   Connection con=ConnectionProvider.getcon();
                   PreparedStatement ps=con.prepareStatement("delete from OrderDetails where orderID='"+order_ID+"'");
                   ps.executeUpdate();
                   JOptionPane.showMessageDialog(null,"Order deleted successfully");
                   setVisible(false);
                   new ViewOrders().setVisible(true);
                   con.close();
                }
                catch(Exception e){
                     JOptionPane.showMessageDialog(null,e);
                }
            }
    }//GEN-LAST:event_viewOrdersMouseClicked

