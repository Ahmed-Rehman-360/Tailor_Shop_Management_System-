    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        if((float)txt_k_length.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Kameez Length");
        }
        else if((float)txt_k_width.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Kameez Width");
        }
        else if((float)txt_sleevesLength.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Sleeves Length");
        }        
        else if((float)txt_sh_length.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Shalwar Length");
        }
        else if((float)txt_sh_waist.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Shalwar Waist");
        }
        else{
            try{
                Connection con=ConnectionProvider.getcon();
                PreparedStatement ps=con.prepareStatement("insert into MeasurementDetails (customerID,shalwarLength,shalwarWaist,kameezLength,kameezWidth,sleevesLength) values (?,?,?,?,?,?)");
                ps.setInt(1,custID);
                ps.setFloat(2,(float)txt_sh_length.getValue());
                ps.setFloat(3,(float)txt_sh_waist.getValue());
                ps.setFloat(4,(float)txt_k_length.getValue());
                ps.setFloat(5,(float)txt_k_width.getValue());
                ps.setFloat(6,(float)txt_sleevesLength.getValue());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Measurements added successfully");
                con.close();
                setVisible(false);
                new Dashboard().setVisible(true);
                }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }        
    }//GEN-LAST:event_submitActionPerformed
    int custID;
    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st1=con.createStatement();
            custID=Integer.parseInt(txt_c_ID.getText());
            ResultSet rs1=st1.executeQuery("select customerName from  CustomerDetails where customerID='"+custID+"'");
            if(rs1.next()){
                    txt_c_name.setText(rs1.getString("customerName"));
                    txt_k_length.setVisible(true);
                    txt_k_width.setVisible(true);
                    txt_sh_length.setVisible(true);
                    txt_sh_waist.setVisible(true);
                    txt_sleevesLength.setVisible(true);
                    jLabel2.setVisible(true);
                    jLabel3.setVisible(true);
                    jLabel4.setVisible(true);
                    jLabel5.setVisible(true);
                    jLabel6.setVisible(true);
                    jLabel7.setVisible(true);
                    jLabel8.setVisible(true);
                    submit.setVisible(true);
                    con.close();
                }
            else{
                JOptionPane.showMessageDialog(null, "No such Customer found");
                txt_c_ID.setText("");
            }
        }
        catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "Please enter an integer in Customer ID field");
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchActionPerformed

    private void btn_viewCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_viewCustomerActionPerformed
        DefaultTableModel model=(DefaultTableModel)viewCustomer.getModel();
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from  CustomerDetails");
            while(rs.next()){
                model.addRow(new Object[]{rs.getInt("customerID"),rs.getString("customerName"),rs.getString("customerPhoneNumber"),rs.getString("customerAddress"),rs.getString("customerGender"),rs.getInt("customerAge")});
            }
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_viewCustomerActionPerformed

