    private void tbl_delCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_delCustomerMouseClicked
        //DefaultTableModel model=(DefaultTableModel)tbl_delCustomer.getModel();
        int index=tbl_delCustomer.getSelectedRow();
        TableModel model=tbl_delCustomer.getModel();
        customerID=(Integer)model.getValueAt(index, 0);
        int a;
        a = JOptionPane.showConfirmDialog(null,"Do you want to delete this customer","Select",JOptionPane.YES_NO_OPTION);
        if(a==0){
            try{
                Connection con=ConnectionProvider.getcon();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select * from  OrderDetails where customerID='"+customerID+"'");
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Can't delete this customer because it has some orders pending");
                }
                else{
                    PreparedStatement ps1=con.prepareStatement("delete from MeasurementDetails where customerID='"+customerID+"'");
                    ps1.executeUpdate();
                    PreparedStatement ps2=con.prepareStatement("delete from CustomerDetails where customerID='"+customerID+"'");
                    ps2.executeUpdate();
                    JOptionPane.showMessageDialog(null,"Customer deleted successfully");
                    con.close();
                    setVisible(false);
                    new DeleteCustomer().setVisible(true);
                }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
        
    }//GEN-LAST:event_tbl_delCustomerMouseClicked

