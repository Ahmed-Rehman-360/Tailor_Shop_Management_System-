    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        txt_c_Name.setEditable(false);
        txt_m_ID.setEditable(false);
        txt_gender.setEnabled(false);
        txt_gender.setSelectedIndex(0);
        txt_dDate.setVisible(false);
        txt_bDate.setVisible(false);
        txt_suits.setVisible(false);
        txt_tAmount.setVisible(false);
        txt_pAmount.setVisible(false);
        txt_rAmount.setVisible(false);
        txt_MaleSuit.setVisible(false);
        txt_FemaleSuit.setVisible(false);
        txt_extraNote.setVisible(false);
        jLabel4.setVisible(false);
        jLabel5.setVisible(false);
        jLabel6.setVisible(false);
        jLabel7.setVisible(false);
        jLabel8.setVisible(false);
        jLabel10.setVisible(false);
        jLabel11.setVisible(false);
        jLabel14.setVisible(false);
        book.setVisible(false);
        try{
            Connection con=ConnectionProvider.getcon();
            Statement st1=con.createStatement();
            custID=Integer.parseInt(txt_c_ID.getText());
            measID=Integer.parseInt(txt_m_ID.getText());
            ResultSet rs1=st1.executeQuery("select * from  CustomerDetails where customerID='"+custID+"'");
            if(rs1.next()){
                Statement st3=con.createStatement();
                ResultSet rs3=st3.executeQuery("select measurementID from MeasurementDetails where measurementID='"+measID+"'");
                if(rs3.next()){
                Statement st2=con.createStatement();
                ResultSet rs2=st2.executeQuery("select measurementID from  MeasurementDetails where customerID='"+custID+"'");
                if(rs2.next()){
                    txt_c_Name.setText(rs1.getString("customerName"));
                    txt_m_ID.setText(rs2.getString("measurementID"));
                    if(rs1.getString("customerGender").equalsIgnoreCase("male")){
                        txt_gender.setSelectedIndex(1);
                        txt_FemaleSuit.setVisible(false);
                        jLabel11.setVisible(true);
                        txt_MaleSuit.setVisible(true);
                    }
                    else{
                        txt_gender.setSelectedIndex(2);
                        txt_MaleSuit.setVisible(false);
                        jLabel11.setVisible(true);
                        txt_FemaleSuit.setVisible(true);
                    }
                }
                else{
                    txt_c_Name.setText(rs1.getString("customerName"));
                    if(rs1.getString("customerGender").equalsIgnoreCase("male")){
                        txt_gender.setSelectedIndex(1);
                    }
                    else{
                        txt_gender.setSelectedIndex(2);
                    }
                    JOptionPane.showMessageDialog(null, "No measurements added for this customer. First add measurement details");
                    con.close();
                }
                }
                else{
                    JOptionPane.showMessageDialog(null, "No measurements found");
                }
            }
            else{
                txt_c_Name.setText("");
                txt_m_ID.setText("");
                JOptionPane.showMessageDialog(null, "No such Customer found");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter an integer in Customer ID field");
            txt_c_ID.setText("");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_searchActionPerformed

    private void bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookActionPerformed
        try{
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String bookedDate =sdf.format(txt_bDate.getDate());
        String deliveryDate =sdf.format(txt_dDate.getDate());
        if(bookedDate.equals("")){
            JOptionPane.showMessageDialog(null, "Please specify booked date");
        }
        else if(deliveryDate.equals("")){
            JOptionPane.showMessageDialog(null, "Please specify delivery date");
        }
        else if((Integer)txt_suits.getValue()==0){
            JOptionPane.showMessageDialog(null, "Suits should be minimum one");
        }
        else if(txt_tAmount.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Total amount field can't be left empty");
        }
        else if(txt_pAmount.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Paid amount field can't be left empty");
        }
        else if(Integer.parseInt(txt_tAmount.getText())==0){
            JOptionPane.showMessageDialog(null, "Total amount should not be zero");
        }
        else if(Integer.parseInt(txt_pAmount.getText())>Integer.parseInt(txt_tAmount.getText())){
            JOptionPane.showMessageDialog(null, "Paid amount should not be more than total amount");
        }
        else if(txt_gender.getSelectedIndex()==1){
            int remAmount=Integer.parseInt(txt_tAmount.getText())-Integer.parseInt(txt_pAmount.getText());
            txt_rAmount.setText(String.valueOf(remAmount));
            Connection con=ConnectionProvider.getcon();
            PreparedStatement ps=con.prepareStatement("insert into OrderDetails (customerID,bookedDate,returnDate,no_of_suits,totalAmount,paidAmount,remainingAmount,extraNote,suitType) values (?,?,?,?,?,?,?,?,?)");
            ps.setInt(1,custID);
            ps.setString(2, bookedDate);
            ps.setString(3, deliveryDate);
            ps.setFloat(4,(Integer)txt_suits.getValue());
            ps.setFloat(5,Integer.parseInt(txt_tAmount.getText()));
            ps.setFloat(6,Integer.parseInt(txt_pAmount.getText()));
            ps.setFloat(7,remAmount);
            ps.setString(8,txt_extraNote.getText());
            ps.setString(9,String.valueOf(txt_MaleSuit.getSelectedItem()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Order saved successfully. Your remaining amount is "+remAmount);
            setVisible(false);
            new Dashboard().setVisible(true);
            con.close();
        }
        else{
            int remAmount=Integer.parseInt(txt_tAmount.getText())-Integer.parseInt(txt_pAmount.getText());
            txt_rAmount.setText(String.valueOf(remAmount));
            Connection con=ConnectionProvider.getcon();
            PreparedStatement ps=con.prepareStatement("insert into OrderDetails (customerID,bookedDate,returnDate,no_of_suits,totalAmount,paidAmount,remainingAmount,extraNote,suitType) values (?,?,?,?,?,?,?,?,?)");
            ps.setInt(1,custID);
            ps.setString(2, bookedDate);
            ps.setString(3, deliveryDate);
            ps.setFloat(4,(Integer)txt_suits.getValue());
            ps.setFloat(5,Integer.parseInt(txt_tAmount.getText()));
            ps.setFloat(6,Integer.parseInt(txt_pAmount.getText()));
            ps.setFloat(7,remAmount);
            ps.setString(8,txt_extraNote.getText());
            ps.setString(9,String.valueOf(txt_FemaleSuit.getSelectedItem()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Order saved successfully. Your remaining amount is "+remAmount);
            setVisible(false);
            new Dashboard().setVisible(true);
            con.close();
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Total,Paid,Remaining amount fields should  be of integer type");
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Please fill entire form");
        }
    }//GEN-LAST:event_bookActionPerformed

