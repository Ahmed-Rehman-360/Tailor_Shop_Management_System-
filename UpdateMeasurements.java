    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if((float)txt_k_length.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Kameez Length");
        }
        else if((float)txt_k_width.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Kameez Width");
        }
        else if((float)txt_sleevesLength.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Sleeves Length");
        }
        else if((float)txt_sh_length.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Shalwar Length");
        }
        else if((float)txt_sh_waist.getValue()==0){
            JOptionPane.showMessageDialog(null, " Please specify correct Shalwar Waist");
        }
        else{
            int a;
            a = JOptionPane.showConfirmDialog(null,"Are you sure to update measurements","Select",JOptionPane.YES_NO_OPTION);
            if(a==0)
            {
            try{
                Connection con=ConnectionProvider.getcon();
                PreparedStatement ps=con.prepareStatement("update MeasurementDetails set shalwarLength=?,shalwarWaist=?,kameezLength=?,kameezWidth=?,sleevesLength=? where measurementID='"+m_ID+"'");
                ps.setFloat(1,(float)txt_sh_length.getValue());
                ps.setFloat(2,(float)txt_sh_waist.getValue());
                ps.setFloat(3,(float)txt_k_length.getValue());
                ps.setFloat(4,(float)txt_k_width.getValue());
                ps.setFloat(5,(float)txt_sleevesLength.getValue());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null,"Measurements updated successfully");
                con.close();
                setVisible(false);
                new UpdateMeasurements().setVisible(true);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            }
        }
    }//GEN-LAST:event_updateActionPerformed

