    private void btn_AddEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddEmployeeActionPerformed
        try{
        SimpleDateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
        String empDOB =dateFormat.format(jdc_EmpDOB.getDate());
        String empDOH =dateFormat.format(jdc_EmpDOH.getDate());        //DOH=Date of Hired
        String empName=txt_EmpName.getText();
        String empPhoneNumber=txt_EmpPhoneNumber.getText();
        String empAddress=txt_EmpAddress.getText();
        String empCNIC=txt_EmpCNIC.getText();
        if(rBtn_EmpMale.isSelected()){
            empGender="Male";
        }
        if(rBtn_EmpFemale.isSelected()){
            empGender="Female";
        }
        if(empName.equals(""))
        JOptionPane.showMessageDialog(null, "Name can't be left empty");
        else if(empPhoneNumber.equals("")||empPhoneNumber.length()!=11)
        JOptionPane.showMessageDialog(null, "Please enter valid mobile number without dash");
        else if(empAddress.equals(""))
        JOptionPane.showMessageDialog(null, "Please enter address field");
        else if(empCNIC.equals("")||empCNIC.length()!=15)
        JOptionPane.showMessageDialog(null, "Please enter correct cnic with dashes");
        else if(!(rBtn_EmpMale.isSelected()||rBtn_EmpFemale.isSelected()))
        JOptionPane.showMessageDialog(null, "Gender is not specified");
        else if(empDOB.equals("")){
            JOptionPane.showMessageDialog(null, "Please specify date of birth");
        }
        else if(empDOB.equals("")){
            JOptionPane.showMessageDialog(null, "Please specify hired date");
        }
        else{
                Connection con=ConnectionProvider.getcon();
                PreparedStatement ps=con.prepareStatement("insert into EmployeeDetails (employeeName,employeeDOB,employeePhoneNumber,employeeCNIC,employeeGender,employeeHiredDate,employeeAddress) values (?,?,?,?,?,?,?)");
                ps.setString(1,empName);
                ps.setString(2,empDOB);
                ps.setString(3,empPhoneNumber);
                ps.setString(4,empCNIC);
                ps.setString(5,empGender);
                ps.setString(6,empDOH);
                ps.setString(7,empAddress);
                ps.executeUpdate();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select employeeID from  EmployeeDetails where employeePhoneNumber='"+empPhoneNumber+"'");
                if(rs.next()){
                    int empID = rs.getInt("employeeID");
                    JOptionPane.showMessageDialog(null,"Employee added successfully.Employee id is: "+empID);
                    setVisible(false);
                    new Dashboard().setVisible(true);
                con.close();
            }
        }   
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        catch(NullPointerException e){
            JOptionPane.showMessageDialog(null, "Please fill entire form");
        }
    }//GEN-LAST:event_btn_AddEmployeeActionPerformed

